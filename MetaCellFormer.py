def build_knn_neighbors_from_embedding(adata, embed_key="X_embed_transformer", n_neighbors=15):
    adata.obsm["X_tmp_for_refine"] = adata.obsm[embed_key].copy()
    sc.pp.neighbors(adata, use_rep="X_tmp_for_refine", n_neighbors=n_neighbors, metric="euclidean")
    knn_graph = adata.obsp["connectivities"].tolil()
    neighbors = {i: knn_graph.rows[i] for i in range(adata.n_obs)}
    return neighbors


def unsupervised_refine_embeddings(adata, device, embed_key="X_embed_transformer", epochs=30, batch_size=256, lr=1e-3, lambda_reg=0.8):
    z0 = adata.obsm[embed_key].copy()
    neighbors = build_knn_neighbors_from_embedding(adata, embed_key=embed_key, n_neighbors=15)

    refiner = RefineMLP(z0.shape[1]).to(device)
    z0_torch = torch.tensor(z0, dtype=torch.float32).to(device)
    optimizer = torch.optim.Adam(refiner.parameters(), lr=lr)

    indices = np.arange(z0.shape[0])
    print("\nStart unsupervised refinement...")

    for epoch in range(epochs):
        np.random.shuffle(indices)
        epoch_loss = 0.0

        for start in range(0, len(indices), batch_size):
            batch_idx = indices[start:start + batch_size]
            batch = torch.tensor(batch_idx, device=device)

            z0_batch = z0_torch[batch]
            z_batch = refiner(z0_batch)

            pos_pairs_i = []
            pos_pairs_j = []
            batch_set = set(batch_idx.tolist()) if hasattr(batch_idx, "tolist") else set(batch_idx)

            for i in batch_idx:
                for j in neighbors[i]:
                    if j in batch_set:
                        pos_pairs_i.append(i)
                        pos_pairs_j.append(j)

            if len(pos_pairs_i) > 0:
                mapping = {cell: k for k, cell in enumerate(batch_idx)}
                pos_i = torch.tensor([mapping[i] for i in pos_pairs_i], device=device)
                pos_j = torch.tensor([mapping[j] for j in pos_pairs_j], device=device)
                loss_pull = F.mse_loss(z_batch[pos_i], z_batch[pos_j])
            else:
                loss_pull = torch.tensor(0.0, device=device)

            loss_reg = F.mse_loss(z_batch, z0_batch)
            loss = loss_pull + lambda_reg * loss_reg

            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            epoch_loss += loss.item() * len(batch_idx)

        epoch_loss /= len(indices)
        print(f"Epoch {epoch + 1}/{epochs} | Loss: {epoch_loss:.4f}")

    with torch.no_grad():
        z_refined = refiner(z0_torch).cpu().numpy()

    adata.obsm["X_embed_unsup_refined"] = z_refined
    sc.pp.neighbors(adata, use_rep="X_embed_unsup_refined", n_neighbors=15)
    sc.tl.leiden(adata, key_added="leiden_unsup_refined", resolution=1.0)
    sc.tl.umap(adata)
    return adata


