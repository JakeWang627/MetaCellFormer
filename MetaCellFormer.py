def parse_args():
    parser = argparse.ArgumentParser(description="MetaCellFormer pipeline from SATURN pretrain output")
    parser.add_argument("--input_h5ad", type=str, required=True, help="Input .h5ad from train-saturn.py pretrain output")
    parser.add_argument("--output_dir", type=str, default="./MetaCellFormer_output", help="Directory to save outputs")
    parser.add_argument("--output_prefix", type=str, default="MetaCellFormer", help="Output file prefix")
    parser.add_argument("--device", type=str, default="cuda" if torch.cuda.is_available() else "cpu")
    parser.add_argument("--seed", type=int, default=2025)

    parser.add_argument("--embedding_dim", type=int, default=256)
    parser.add_argument("--hidden_dim", type=int, default=256)
    parser.add_argument("--num_heads", type=int, default=4)
    parser.add_argument("--num_layers", type=int, default=1)
    parser.add_argument("--batch_size", type=int, default=1024)
    parser.add_argument("--epochs", type=int, default=50)
    parser.add_argument("--lr", type=float, default=1e-3)
    parser.add_argument("--n_centers", type=int, default=2)
    parser.add_argument("--lambda_var", type=float, default=0.2)
    parser.add_argument("--lambda_mmd", type=float, default=0.2)
    parser.add_argument("--num_workers", type=int, default=0)

    parser.add_argument("--umap_n_neighbors", type=int, default=15)
    parser.add_argument("--umap_min_dist", type=float, default=0.5)
    parser.add_argument("--leiden_resolution", type=float, default=1.0)
    parser.add_argument("--large_type_threshold", type=int, default=3000)

    parser.add_argument("--run_merge_refine", action="store_true", help="Run merge-group refinement")
    parser.add_argument("--merge_percentile", type=float, default=5.0, help="Percentile threshold for auto merge proposal")
    parser.add_argument("--merge_epochs", type=int, default=30)
    parser.add_argument("--merge_batch_size", type=int, default=2048)
    parser.add_argument("--merge_lr", type=float, default=1e-3)
    parser.add_argument("--merge_lambda_reg", type=float, default=0.1)

    parser.add_argument("--run_unsup_refine", action="store_true", help="Run unsupervised refinement")
    parser.add_argument("--unsup_epochs", type=int, default=30)
    parser.add_argument("--unsup_batch_size", type=int, default=256)
    parser.add_argument("--unsup_lr", type=float, default=1e-3)
    parser.add_argument("--unsup_lambda_reg", type=float, default=0.8)

    return parser.parse_args()


def main():
    args = parse_args()
    set_seed(args.seed)

    device = torch.device(args.device)
    print("Using device:", device)

    output_dir = Path(args.output_dir)
    figures_dir = output_dir / "figures"
    ensure_dir(output_dir)
    ensure_dir(figures_dir)

    # 1. Load data
    adata, features, labels, species_labels, _, _ = load_and_prepare_data(args.input_h5ad)

    # 2. Train model
    model, _, history = train_embedding_model(
        features=features,
        labels=labels,
        species_labels=species_labels,
        device=device,
        embedding_dim=args.embedding_dim,
        batch_size=args.batch_size,
        epochs=args.epochs,
        lr=args.lr,
        n_centers=args.n_centers,
        lambda_var=args.lambda_var,
        lambda_mmd=args.lambda_mmd,
        num_heads=args.num_heads,
        num_layers=args.num_layers,
        hidden_dim=args.hidden_dim,
        num_workers=args.num_workers,
    )

    # 3. Save training curve
    plot_loss_curves(history, figures_dir / f"{args.output_prefix}_loss_curve.png")

    # 4. Infer embeddings
    print("\nGenerating embeddings for all cells...")
    all_embeddings = infer_embeddings(model, features, args.batch_size, device)
    print("Embedding shape:", all_embeddings.shape)
    adata.obsm["X_embed_transformer"] = all_embeddings

    # 5. UMAP + Leiden
    print("Running UMAP and Leiden clustering...")
    adata = run_umap_and_leiden(
        adata,
        embed_key="X_embed_transformer",
        seed=args.seed,
        resolution=args.leiden_resolution,
        n_neighbors=args.umap_n_neighbors,
        min_dist=args.umap_min_dist,
        leiden_key="leiden_transformer",
    )

    # 6. Save basic plots
    save_umap_plot(
        adata,
        color="species",
        out_path=figures_dir / f"{args.output_prefix}_umap_species.png",
        title="UMAP by Species (Transformer + SoftTriple + MMD)",
    )
    save_umap_plot(
        adata,
        color="labels2",
        out_path=figures_dir / f"{args.output_prefix}_umap_labels2.png",
        title="UMAP by Cell Types (Transformer + SoftTriple + MMD)",
    )
    save_umap_plot(
        adata,
        color="leiden_transformer",
        out_path=figures_dir / f"{args.output_prefix}_umap_leiden_transformer.png",
        title="UMAP by Leiden (Transformer + SoftTriple + MMD)",
    )
    save_large_small_label_plots(adata, args.large_type_threshold, figures_dir)

    # 7. Optional merge-group refinement
    if args.run_merge_refine:
        merge_groups = propose_merge_groups(
            adata,
            cluster_key="leiden_transformer",
            umap_key="X_umap",
            percentile=args.merge_percentile,
        )
        if len(merge_groups) > 0:
            adata = build_meta_clusters(adata, merge_groups, cluster_key="leiden_transformer")
            adata = refine_with_merge_groups(
                adata,
                device=device,
                epochs=args.merge_epochs,
                batch_size=args.merge_batch_size,
                lr=args.merge_lr,
                lambda_reg=args.merge_lambda_reg,
            )
            save_umap_plot(
                adata,
                color="leiden_merged",
                out_path=figures_dir / f"{args.output_prefix}_umap_leiden_merged.png",
                title="After merging refinement (leiden_merged)",
            )
        else:
            print("No merge groups detected; skipping merge refinement.")

    # 8. Optional unsupervised refinement
    if args.run_unsup_refine:
        adata = unsupervised_refine_embeddings(
            adata,
            device=device,
            embed_key="X_embed_transformer",
            epochs=args.unsup_epochs,
            batch_size=args.unsup_batch_size,
            lr=args.unsup_lr,
            lambda_reg=args.unsup_lambda_reg,
        )
        save_umap_plot(
            adata,
            color="leiden_unsup_refined",
            out_path=figures_dir / f"{args.output_prefix}_umap_leiden_unsup_refined.png",
            title="After unsupervised refinement (leiden_unsup_refined)",
        )

    # 9. Save output h5ad
    output_h5ad = output_dir / f"{args.output_prefix}.h5ad"
    adata.write(output_h5ad)
    print(f"\nFinal AnnData saved to: {output_h5ad}")

    # 10. Save model weights
    model_path = output_dir / f"{args.output_prefix}_model.pt"
    torch.save(model.state_dict(), model_path)
    print(f"Model weights saved to: {model_path}")


if __name__ == "__main__":
    main()
