class EmbeddingModel(nn.Module):
    """Transformer encoder for cell embeddings."""

    def __init__(self, input_dim, embedding_dim=256, num_heads=4, num_layers=1, hidden_dim=256, dropout=0.2):
        super().__init__()
        self.input_proj = nn.Linear(input_dim, hidden_dim)
        encoder_layer = nn.TransformerEncoderLayer(
            d_model=hidden_dim,
            nhead=num_heads,
            dim_feedforward=hidden_dim * 4,
            batch_first=True,
            dropout=dropout,
        )
        self.encoder = nn.TransformerEncoder(encoder_layer, num_layers=num_layers)
        self.out_proj = nn.Linear(hidden_dim, embedding_dim)

    def forward(self, x):
        # x: [B, D] -> [1, B, H]
        x = self.input_proj(x)
        x = x.unsqueeze(0)
        x = self.encoder(x)
        x = self.out_proj(x).squeeze(0)
        return x


class RefineMLP(nn.Module):
    def __init__(self, dim):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(dim, dim),
            nn.ReLU(),
            nn.Linear(dim, dim),
        )

    def forward(self, x):
        return self.net(x)


class CenterLoss(nn.Module):
    def __init__(self, num_classes, feat_dim):
        super().__init__()
        self.centers = nn.Parameter(torch.randn(num_classes, feat_dim))

    def forward(self, feats, labels):
        centers_batch = self.centers[labels]
        return F.mse_loss(feats, centers_batch)


