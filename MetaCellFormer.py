class SoftTripleLoss(nn.Module):
    """Simplified SoftTriple loss."""

    def __init__(self, embedding_dim, n_classes, n_centers=5, la=10.0, gamma=0.1, tau=0.2):
        super().__init__()
        self.la = la
        self.gamma = gamma
        self.tau = tau
        self.n_classes = n_classes
        self.n_centers = n_centers

        self.centers = nn.Parameter(
            torch.randn(n_classes * n_centers, embedding_dim) / np.sqrt(embedding_dim)
        )
        self.weight = nn.Parameter(torch.ones(n_classes, n_centers) / n_centers)

    def forward(self, x, labels):
        x = F.normalize(x, dim=1)
        centers = F.normalize(self.centers, dim=1)

        sim = torch.matmul(x, centers.t())
        batch_size = x.size(0)
        sim = sim.view(batch_size, self.n_classes, self.n_centers)

        weight = F.softmax(self.weight, dim=1)
        sim_weighted = torch.sum(sim * weight.unsqueeze(0), dim=2)

        mask = torch.zeros_like(sim_weighted).scatter_(1, labels.unsqueeze(1), 1)
        sim_target = torch.sum(sim_weighted * mask, dim=1)
        sim_others = torch.max(sim_weighted * (1 - mask), dim=1)[0]

        loss = torch.mean(F.softplus(self.la * (sim_others - sim_target + self.gamma)))
        return loss


def variance_loss(embeddings, labels):
    """Within-class compactness loss."""
    unique_labels = labels.unique()
    loss = torch.tensor(0.0, device=embeddings.device)
    count = 0

    for lab in unique_labels:
        mask = labels == lab
        cluster = embeddings[mask]
        if cluster.size(0) < 2:
            continue
        center = cluster.mean(dim=0, keepdim=True)
        loss = loss + ((cluster - center) ** 2).sum(dim=1).mean()
        count += 1

    if count == 0:
        return torch.tensor(0.0, device=embeddings.device)
    return loss / count


def mmd_loss(source, target, sigma=1.0):
    """RBF-kernel MMD."""
    if source.size(0) == 0 or target.size(0) == 0:
        return torch.tensor(0.0, device=source.device)

    def gaussian_kernel(x, y, sigma_):
        x_norm = (x ** 2).sum(dim=1).view(-1, 1)
        y_norm = (y ** 2).sum(dim=1).view(1, -1)
        dist = x_norm + y_norm - 2.0 * torch.mm(x, y.t())
        return torch.exp(-dist / (2 * sigma_ ** 2))

    k_xx = gaussian_kernel(source, source, sigma)
    k_yy = gaussian_kernel(target, target, sigma)
    k_xy = gaussian_kernel(source, target, sigma)

    return k_xx.mean() + k_yy.mean() - 2 * k_xy.mean()


def cross_species_mmd(embeddings, labels, species_labels, sigma=1.0):
    """MMD alignment across species within each cell type."""
    device = embeddings.device
    unique_labels = labels.unique()
    total_mmd = torch.tensor(0.0, device=device)
    count = 0

    for lab in unique_labels:
        mask_lab = labels == lab
        if mask_lab.sum() < 2:
            continue

        emb_lab = embeddings[mask_lab]
        species_lab = species_labels[mask_lab]
        unique_species = species_lab.unique()
        if unique_species.numel() < 2:
            continue

        for i in range(len(unique_species)):
            for j in range(i + 1, len(unique_species)):
                si = unique_species[i]
                sj = unique_species[j]
                emb_i = emb_lab[species_lab == si]
                emb_j = emb_lab[species_lab == sj]
                if emb_i.size(0) == 0 or emb_j.size(0) == 0:
                    continue
                total_mmd = total_mmd + mmd_loss(emb_i, emb_j, sigma=sigma)
                count += 1

    if count == 0:
        return torch.tensor(0.0, device=device)
    return total_mmd / count


