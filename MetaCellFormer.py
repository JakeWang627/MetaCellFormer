def load_and_prepare_data(input_h5ad: str):
    adata = sc.read(input_h5ad)
    print("Loaded AnnData:", adata)

    required_cols = ["labels2", "species"]
    missing = [c for c in required_cols if c not in adata.obs.columns]
    if missing:
        raise ValueError(f"Input h5ad is missing required obs columns: {missing}")

    adata = adata[~adata.obs["labels2"].isna() & ~adata.obs["species"].isna()].copy()

    label_encoder = LabelEncoder()
    adata.obs["labels2_encoded"] = label_encoder.fit_transform(adata.obs["labels2"])
    n_classes = len(label_encoder.classes_)

    species_encoder = LabelEncoder()
    adata.obs["species_encoded"] = species_encoder.fit_transform(adata.obs["species"])
    n_species = len(species_encoder.classes_)

    # Standardize adata.X
    sc.pp.scale(adata)

    features = adata.X.A if hasattr(adata.X, "A") else adata.X
    features = np.asarray(features, dtype=np.float32)
    labels = adata.obs["labels2_encoded"].values
    species_labels = adata.obs["species_encoded"].values

    print(f"Number of classes: {n_classes}")
    print(f"Number of species: {n_species}")
    print(f"Feature matrix shape: {features.shape}")

    return adata, features, labels, species_labels, label_encoder, species_encoder


