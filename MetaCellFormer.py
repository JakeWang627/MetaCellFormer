def train_embedding_model(
    features,
    labels,
    species_labels,
    device,
    embedding_dim=256,
    batch_size=1024,
    epochs=50,
    lr=1e-3,
    n_centers=2,
    lambda_var=0.2,
    lambda_mmd=0.2,
    num_heads=4,
    num_layers=1,
    hidden_dim=256,
    num_workers=0,
):
    dataset = SingleCellDataset(features, labels, species_labels)
    dataloader = DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=torch.cuda.is_available(),
    )

    model = EmbeddingModel(
        input_dim=features.shape[1],
        embedding_dim=embedding_dim,
        num_heads=num_heads,
        num_layers=num_layers,
        hidden_dim=hidden_dim,
    ).to(device)

    criterion = SoftTripleLoss(
        embedding_dim=embedding_dim,
        n_classes=len(np.unique(labels)),
        n_centers=n_centers,
    ).to(device)

    optimizer = torch.optim.Adam(list(model.parameters()) + list(criterion.parameters()), lr=lr)
    scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
        optimizer, mode="min", patience=5, factor=0.5
    )

    loss_history = []
    soft_history = []
    var_history = []
    mmd_history = []

    print("\nStart training...")
    for epoch in range(epochs):
        model.train()
        running_total = 0.0
        running_soft = 0.0
        running_var = 0.0
        running_mmd = 0.0
        seen = 0

        for batch_features, batch_labels, batch_species in dataloader:
            batch_features = batch_features.to(device)
            batch_labels = batch_labels.to(device)
            batch_species = batch_species.to(device)

            embeddings = model(batch_features)
            loss_softtriple = criterion(embeddings, batch_labels)
            loss_var = variance_loss(embeddings, batch_labels)
            loss_mmd = cross_species_mmd(embeddings, batch_labels, batch_species, sigma=1.0)
            loss = loss_softtriple + lambda_var * loss_var + lambda_mmd * loss_mmd

            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            bs = batch_features.size(0)
            seen += bs
            running_total += loss.item() * bs
            running_soft += loss_softtriple.item() * bs
            running_var += loss_var.item() * bs
            running_mmd += loss_mmd.item() * bs

        epoch_loss = running_total / seen
        epoch_soft = running_soft / seen
        epoch_var = running_var / seen
        epoch_mmd = running_mmd / seen

        loss_history.append(epoch_loss)
        soft_history.append(epoch_soft)
        var_history.append(epoch_var)
        mmd_history.append(epoch_mmd)
        scheduler.step(epoch_loss)

        print(
            f"Epoch [{epoch + 1}/{epochs}] | "
            f"Loss: {epoch_loss:.4f} | "
            f"SoftTriple: {epoch_soft:.4f} | "
            f"Var: {epoch_var:.4f} | "
            f"MMD: {epoch_mmd:.4f}"
        )

    history = {
        "total": loss_history,
        "softtriple": soft_history,
        "variance": var_history,
        "mmd": mmd_history,
    }
    return model, criterion, history


def infer_embeddings(model, features, batch_size, device):
    model.eval()
    all_embeddings = []
    with torch.no_grad():
        for i in range(0, len(features), batch_size):
            batch_features = torch.tensor(features[i:i + batch_size], dtype=torch.float32).to(device)
            emb = model(batch_features)
            all_embeddings.append(emb.cpu().numpy())
    return np.vstack(all_embeddings)


