def propose_merge_groups(adata, cluster_key="leiden_transformer", umap_key="X_umap", percentile=5):
    clusters = adata.obs[cluster_key].astype(str).values
    unique_clusters = np.unique(clusters)
    umap_coords = adata.obsm[umap_key]

    centroids = []
    for c in unique_clusters:
        idx = np.where(clusters == c)[0]
        centroids.append(umap_coords[idx].mean(axis=0))
    centroids = np.vstack(centroids)

    dist_matrix = pairwise_distances(centroids, metric="euclidean")

    pairs = []
    for i in range(len(unique_clusters)):
        for j in range(i + 1, len(unique_clusters)):
            pairs.append((unique_clusters[i], unique_clusters[j], dist_matrix[i, j]))

    pairs_sorted = sorted(pairs, key=lambda x: x[2])
    top_pairs = pairs_sorted[:10]
    print("Top 10 closest cluster pairs by UMAP centroid distance:")
    for a, b, d in top_pairs:
        print(f"  Cluster {a} <-> {b} | dist = {d:.4f}")

    threshold = np.percentile(dist_matrix, percentile)
    edges = defaultdict(set)
    for a, b, d in pairs:
        if d <= threshold:
            edges[a].add(b)
            edges[b].add(a)

    visited = set()
    merge_groups = []

    def dfs(node, group):
        visited.add(node)
        group.append(node)
        for nei in edges[node]:
            if nei not in visited:
                dfs(nei, group)

    for node in list(edges.keys()):
        if node not in visited:
            group = []
            dfs(node, group)
            if len(group) >= 2:
                merge_groups.append(group)

    print("Suggested merge_groups:", merge_groups)
    return merge_groups


def build_meta_clusters(adata, merge_groups, cluster_key="leiden_transformer"):
    adata.obs[cluster_key] = adata.obs[cluster_key].astype(str)
    cluster2meta = {}
    for i, group in enumerate(merge_groups):
        meta_label = f"meta_{i}"
        for c in group:
            cluster2meta[c] = meta_label

    meta_list = []
    for c in adata.obs[cluster_key]:
        if c in cluster2meta:
            meta_list.append(cluster2meta[c])
        else:
            meta_list.append(f"keep_{c}")

    adata.obs["meta_cluster"] = pd.Categorical(meta_list)
    return adata


def refine_with_merge_groups(adata, device, epochs=30, batch_size=2048, lr=1e-3, lambda_reg=0.1):
    z0 = adata.obsm["X_embed_transformer"].copy()
    meta_labels = adata.obs["meta_cluster"].cat.codes.values

    dataset = EmbeddingDataset(z0, meta_labels)
    dataloader = DataLoader(dataset, batch_size=batch_size, shuffle=True)

    dim = z0.shape[1]
    refiner = RefineMLP(dim).to(device)
    center_loss_fn = CenterLoss(len(np.unique(meta_labels)), dim).to(device)

    optimizer = torch.optim.Adam(list(refiner.parameters()) + list(center_loss_fn.parameters()), lr=lr)

    print("\nStart merge-group refinement...")
    for epoch in range(epochs):
        epoch_loss = 0.0
        for xb, yb in dataloader:
            xb = xb.to(device)
            yb = yb.to(device)

            z = refiner(xb)
            loss_c = center_loss_fn(z, yb)
            loss_r = F.mse_loss(z, xb)
            loss = loss_c + lambda_reg * loss_r

            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            epoch_loss += loss.item() * xb.size(0)

        epoch_loss /= len(dataset)
        print(f"Epoch {epoch + 1}/{epochs} | Loss = {epoch_loss:.4f}")

    with torch.no_grad():
        z_refined = refiner(torch.tensor(z0, dtype=torch.float32).to(device)).cpu().numpy()

    adata.obsm["X_embed_merged"] = z_refined
    sc.pp.neighbors(adata, use_rep="X_embed_merged", n_neighbors=15)
    sc.tl.leiden(adata, key_added="leiden_merged", resolution=1.0)
    sc.tl.umap(adata)
    return adata


