def run_umap_and_leiden(adata, embed_key, seed, resolution=1.0, n_neighbors=15, min_dist=0.5, leiden_key="leiden_transformer"):
    reducer = umap.UMAP(
        n_neighbors=n_neighbors,
        min_dist=min_dist,
        n_components=2,
        random_state=seed,
    )
    umap_embeddings = reducer.fit_transform(adata.obsm[embed_key])
    adata.obsm["X_umap"] = umap_embeddings

    sc.pp.neighbors(adata, use_rep=embed_key, random_state=seed)
    sc.tl.leiden(adata, resolution=resolution, key_added=leiden_key, random_state=seed)
    return adata


